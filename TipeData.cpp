#include <iostream>

using namespace std ;

main () 
{
	short a = 100 ;
	long b = 9876543210 ;
	int c = 1998 ;
	float d = 3.14 ;
	double e = 3.14057 ;
	char f = 'X' ;
	char g[] = "Lorem Ipsum Sit Dolor Amet" ;
	string h = " This Is String Type " ;

	cout << " This Is Short Type Data : " << a << endl ;
	cout << " This Is Long Type Data : " << b << endl ; 
	cout << " This Is Integer Type Data : " << c << endl ;
	cout << " This Is Float Type Data : " << d << endl ;  
	cout << " This Is Double Type Data : " << e << endl ; 
	cout << " This Is Char Type Data : " << f << endl ; 
	cout << " This Is Many Char Type Data : " << g << endl ; 
	cout << " This Is String Type Data : " << h << endl ; 
}