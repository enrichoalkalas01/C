#include <iostream>

using namespace std ;

main ()
{
	int x = 10;

	x += 5;
	cout << "This Is Result Of x += 5 : " << x << endl ;

	x -= 10;
	cout << "This Is Result Of x -= 10 : " << x << endl ;

	x *= 2;
	cout << "This Is Result Of x *= 2 : " << x << endl ;

	x /= 4;
	cout << "This Is Result Of x /= 4 : " << x << endl ;
}