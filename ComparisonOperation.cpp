#include <iostream>

using namespace std ;

main () 
{

	int x = 10;
	int y = 20;
	
	int result = x > y;
	cout << result << endl ;

	result = x < y;
	cout << result << endl ;

	result = x >= y;
	cout << result << endl ;

	result = x <= y;
	cout << result << endl ;

	result = y == x;
	cout << result << endl ;

	result = y != x;
	cout << result << endl ;
}